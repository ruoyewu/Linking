public class Box {
    public float width;
    public float height;
    public float startX;
    public float startY;
    public int color;

    public Box(float width, float height, float startX, float startY, int color) {
        this.width = width;
        this.height = height;
        this.startX = startX;
        this.startY = startY;
        this.color = color;
    }
}
